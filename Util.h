	glm::vec4 getCorLuz() {
		std::string ficheirodefinicoesjsonstr = this->lerFicheiro("definicoes.json");
		const char* ficheirodefinicoesjson = ficheirodefinicoesjsonstr.c_str();
		glm::vec4 finalres = glm::vec4(1.0f);
		rapidjson::Document definicoesjson;
		if (definicoesjson.Parse(ficheirodefinicoesjson).HasParseError()) {
			std::cout << definicoesjson.GetParseError();
			return finalres;
		}

		if (!definicoesjson.IsObject()) {
			return finalres;
		}

		float arrvec[3];
		for (int i = 0; i < 3; i++) {
			if (definicoesjson["corluz"][i].GetFloat() < 0.0f) {
				arrvec[i] = 0.0f;
			}
			else if (definicoesjson["corluz"][i].GetFloat() > 1.0f) {
				arrvec[i] = 1.0f;
			}
			else arrvec[i] = definicoesjson["corluz"][i].GetFloat();
		}
		
		return glm::vec4(arrvec[0], arrvec[1], arrvec[2], 1.0f);
	}
